let x; // объявление переменной
console.log(x) // значение undefined

x = 0 // присвоение числового значения
console.log(x) // значение 0

x = 0.01 // присвоение числа с плавающей точкой
console.log(x)

x = "Hello world!" // присвоение строки
console.log(x)

x = true // присвоение значения true типа boolean
console.log(x)

x = false // присвоение значения false типа boolean
console.log(x)

x = null // присвоение специального значения null
console.log(x)

x = undefined // присвоение специального значения undefined
console.log(x)

x = {} // присвоение пустого объекта
console.log(x)